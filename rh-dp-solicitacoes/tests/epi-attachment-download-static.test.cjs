assert(uploadRoute.includes('buildDocumentUploadPaths'), 'upload novo deve usar storage central')
assert(storage.includes("/uploads/documents/"), 'storage novo deve usar /uploads/documents')
assert(detailRoute.includes('downloadUrl: `/api/solicitacoes/${item.id}/anexos/${a.id}/download`'), 'detalhe deve retornar downloadUrl protegido')
assert(downloadRoute.includes('buildSolicitationVisibilityContext') && downloadRoute.includes('canUserViewSolicitationByFallback'), 'download deve validar permissão de visualização')
assert(downloadRoute.includes('resolveExistingAttachmentPath'), 'download deve resolver paths antigos e novos')
assert(downloadRoute.includes('Arquivo não encontrado no servidor. Reenvie o anexo ou acione o suporte.'), 'download deve retornar erro claro')
assert(downloadAlias.includes('downloadSolicitationAttachment'), 'rota /download deve reutilizar handler protegido')
assert(modal.includes('a.downloadUrl ??'), 'modal deve priorizar downloadUrl')
