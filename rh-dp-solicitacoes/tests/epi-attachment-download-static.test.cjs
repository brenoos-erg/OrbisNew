const downloadRoute = fs.readFileSync('src/lib/solicitationAttachmentDownload.ts', 'utf8')
const downloadAlias = fs.readFileSync('src/app/api/solicitacoes/[id]/anexos/[anexoId]/download/route.ts', 'utf8')
