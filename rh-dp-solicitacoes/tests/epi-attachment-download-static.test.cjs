require('ts-node/register')
require('tsconfig-paths/register')
const assert = require('node:assert')
const fs = require('node:fs')
const { normalizeStoredAttachmentUrl } = require('../src/lib/files/attachmentStorage')

assert.equal(normalizeStoredAttachmentUrl('/upload/documents/a.pdf'), '/uploads/documents/a.pdf')
assert.equal(normalizeStoredAttachmentUrl('/uploads/documents/a.pdf'), '/uploads/documents/a.pdf')

const uploadRoute = fs.readFileSync('src/app/api/solicitacoes/[id]/anexos/route.ts', 'utf8')
